function [h] = HGSh(a,T)
%**************************************************************************
%
% [h] = HGSh(a,T)
%
%**************************************************************************
% 
% HGSh calculates the enthalpy of a species using his Burcat coeficients
% and temperature
%
%**************************************************************************
% Inputs:
%--------------------------------------------------------------------------
% a --> Burcat coefficients
% T --> [K] Temperature
%
% Outputs:
%--------------------------------------------------------------------------
% h --> [kJ/mol] Enthalpy
%
%**************************************************************************
% *HGS 2.1
% *By Caleb Fuster, Manel Soria and Arnau Miró
% *ESEIAAT UPC    

global R; HGSr

h = R * (a(6) + sum(a(1:5).*(T.^(1:5))./(1:5))); % [kJ/mol]

end